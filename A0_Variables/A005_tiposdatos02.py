""" Determinar los tipos de datos que estamos usando"""

print('Imprimiendo los tipos cadena')
print(type('Hola a todos'))
print(type('esto es una cadena'))

print('Imprimiendo los tipos enteros')
print(type(123_123))
print(type(123456))

print('Imprimiendo los tipos flotantes')
print(type(3.1416))
print(type(234_567.33333))

print('Imprimiendo los tipos booleanos')
print(type(False))
print(type(True))