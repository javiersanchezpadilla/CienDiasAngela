""" Operaciones matematicas en python"""

print('OPERACIONES MATEMATICAS\n')
print('Manejo de la suma')
print(100 + 50)
print(2.366 + 5.66666)

print('\nResta:')
print(10 - 6)
print(10.22 - 3.333)

print('\nMultiplicación')
print(10 * 2)
print(1.111 * 3.365)

# Division por defecto regresa un dato convertido a flotante
print('\nDivisión\n')
print(10 / 2)
print(type(10 / 2))

print(10 / 3)
print(type(10 / 3))

# Division entera
print('\nDivisión a piso (parte entera)')
print(10 // 2)
print(type(10 // 2))

print(10 // 3)
print(type(10 // 3))

print('Manejo del operador modulo')
print(5 % 2)
print(1 % 3)

print('\nManejo del exponente')
print(2 ** 2)
print(2 ** 3)
