""" OPERADORES LÓGICOS
    
    Uso de los opeadores lógicos
    AND
    OR
    NOT
    
"""

print("Valoración de AND")
print(True and True and True and True)
print(True and True and True and False)

print("Valoración de OR")
print(True or True or True or True or True)
print(True or True or True or True or False)
print(False or False or False or False or True)

print("Valoración de NOT")
print(not(True))
print(not(False))